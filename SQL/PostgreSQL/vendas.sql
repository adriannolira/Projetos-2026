SELECT
	VENDEDOR,
	SUM(VALOR) AS TOTAL_VENDIDO
FROM
	VENDAS
GROUP BY
	VENDEDOR
ORDER BY
	TOTAL_VENDIDO DESC;

SELECT
	CIDADE,
	SUM(VALOR) AS TOTAL_CIDADE
FROM
	VENDAS
GROUP BY
	CIDADE
ORDER BY
	TOTAL_CIDADE DESC;

SELECT
	VENDEDOR,
	SUM(VALOR) AS TOTAL_VENDIDO
FROM
	VENDAS
GROUP BY
	VENDEDOR
HAVING
	SUM(VALOR) > 2000
ORDER BY
	TOTAL_VENDIDO DESC;

SELECT
	VENDEDOR,
	COUNT(*) AS TOTAL_VENDAS_VENDEDOR
FROM
	VENDAS
GROUP BY
	VENDEDOR
ORDER BY
	TOTAL_VENDAS_VENDEDOR DESC;

SELECT
	COUNT(DISTINCT VENDEDOR) AS TOTAL_VENDEDORES
FROM
	VENDAS;